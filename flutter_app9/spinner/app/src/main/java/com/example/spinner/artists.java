package com.example.spinner;

class artists {
    String id;
    String name;
    String About;
    public artists(){

    }




    public artists(String id, String name, String About){
        this.id=id;
        this.name=name;
        this.About=About;

    }
    public String getId() {
        return id;
    }


    public String getName() {
        return name;
    }

    public String getAbout() {
        return About;
    }

}
