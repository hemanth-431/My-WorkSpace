package com.example.dropdown;

public class artists {
    String id;
    String name;
    String gen;
    public artists(){

    }




    public artists(String id, String name, String gen){
        this.id=id;
        this.name=name;
        this.gen=gen;

    }
    public String getId() {
        return id;
    }


    public String getName() {
        return name;
    }


    public String getGen() {
        return gen;
    }

}
